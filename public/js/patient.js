export default class Patient {

    nom = "";
    maladie = "";
    argent = 0;
    poche = "";
    étatSanté = "";

    constructor(nom, maladie, argent, poche, étatSanté) {
        this.nom = nom;
        this.maladie = maladie;
        this.argent = argent;
        this.poche = poche;
        this.étatSanté = étatSanté;
    }

}

